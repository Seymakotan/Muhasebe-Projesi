    private void butceonayyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butceonayyActionPerformed

        // TODO add your handling code here:
        Bolum bolumtemp = new Bolum();
        for (int i = 0; i < Universite.fakulteler.size(); i++) {
            for (int j = 0; j < Universite.fakulteler.get(i).bolumler.size(); j++) {
                if (Universite.fakulteler.get(i).bolumler.get(j).ad.equals(bolum_box.getSelectedItem().toString())) {
                    bolumtemp = Universite.fakulteler.get(i).bolumler.get(j);
                }
            }
        }
        int a = JOptionPane.showConfirmDialog(this, "Butceyi Onayliyor Musunuz?");
        if (a == JOptionPane.YES_OPTION) {
            if (Gider.onaylananBolumler.size() == 0) {
                dlm.addElement(bolumtemp.ad);
                Gider.onaylananBolumler.add(bolumtemp);

            } else {

                for (int i = 0; i < Gider.onaylananBolumler.size(); i++) {
                    if (Gider.onaylananBolumler.get(i).ad.equals(bolumtemp.ad)) {
                        JOptionPane.showMessageDialog(this, "Bu Butce Daha Once Onaylanmıstır");
                        return;
                    }

                }
                dlm.addElement(bolumtemp.ad);
                Gider.onaylananBolumler.add(bolumtemp);
            }

            // dlm.add(0, bolumtemp.ad);
            this.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);

        }


    }//GEN-LAST:event_butceonayyActionPerformed


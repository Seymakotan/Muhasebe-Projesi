    private void butonsilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butonsilActionPerformed

        int k = jTable1.getSelectedRow();

        dtm2.getDataVector().removeAllElements();

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        Bolum bolumtemp = new Bolum();
        for (int i = 0; i < Universite.fakulteler.size(); i++) {
            for (int j = 0; j < Universite.fakulteler.get(i).bolumler.size(); j++) {
                if (Universite.fakulteler.get(i).bolumler.get(j).ad.equals(bolum_box.getSelectedItem().toString())) {
                    bolumtemp = Universite.fakulteler.get(i).bolumler.get(j);
                    for (int l = 0; l < bolumtemp.giderler.size(); l++) {
                        if (bolumtemp.giderler.get(l).urun_adi.equals(model.getValueAt(k, 0).toString())) {
                            bolumtemp.silinengiderler.add(bolumtemp.giderler.get(l));
                            bolumtemp.giderler.remove(bolumtemp.giderler.get(l));
                            model.removeRow(k);
                        }
                    }
                    text_urunmiktar.setText("");
                    text_fiyat.setText("");
                }
            }
        }

        for (int i = 0; i < bolumtemp.silinengiderler.size(); i++) {
            dtm2.addRow(new Object[]{bolumtemp.silinengiderler.get(i).urun_adi, bolumtemp.silinengiderler.get(i).birim, String.valueOf(bolumtemp.silinengiderler.get(i).miktar),
                String.valueOf(bolumtemp.silinengiderler.get(i).fiyat), String.valueOf(bolumtemp.silinengiderler.get(i).toplamgider)});

        }

        giderTopla();


    }//GEN-LAST:event_butonsilActionPerformed


    void giderTopla() {
        try {
            int toplam = 0;
            Bolum bolumtemp1 = new Bolum();
            for (int i = 0; i < Universite.fakulteler.size(); i++) {
                for (int j = 0; j < Universite.fakulteler.get(i).bolumler.size(); j++) {

                    if (Universite.fakulteler.get(i).bolumler.get(j).ad.equals(bolum_box.getSelectedItem().toString())) {
                        bolumtemp1 = Universite.fakulteler.get(i).bolumler.get(j);
                    }
                }
            }
            for (int i = 0; i < bolumtemp1.giderler.size(); i++) {
                toplam += bolumtemp1.giderler.get(i).toplamgider;
            }
            lbl_toplam.setText(String.valueOf(toplam));

        } catch (NullPointerException n) {

        }
    }

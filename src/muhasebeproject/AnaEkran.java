        dtm.setRowCount(0);
        dtm2.setRowCount(0);
        //dtm.getDataVector().removeAllElements();
        //dtm2.getDataVector().removeAllElements();
        Bolum tempb = new Bolum();
        Fakulte tempf = new Fakulte();

        for (int i = 0; i < Universite.fakulteler.size(); i++) {
            if (fakulte_box.getSelectedItem() != null) {
                if (Universite.fakulteler.get(i).ad.equals(fakulte_box.getSelectedItem().toString())) {
                    tempf = Universite.fakulteler.get(i);
                }
            }
        }
        for (int i = 0; i < tempf.bolumler.size(); i++) {
            if (bolum_box.getSelectedItem() != null) {
                if (tempf.bolumler.get(i).ad.equals(bolum_box.getSelectedItem().toString())) {
                    tempb = tempf.bolumler.get(i);

                }
            }
        }

        for (int i = 0; i < tempb.giderler.size(); i++) {
            if (tempb.giderler.get(i) != null) {
                // toplam += tempb.giderler.get(i).toplamgider;
                dtm.addRow(new String[]{tempb.giderler.get(i).urun_adi, tempb.giderler.get(i).birim, String.valueOf(tempb.giderler.get(i).miktar),
                    String.valueOf(tempb.giderler.get(i).fiyat), String.valueOf(tempb.giderler.get(i).toplamgider)});
            }
        }

        for (int i = 0; i < tempb.silinengiderler.size(); i++) {
            if (tempb.silinengiderler.get(i) != null) {
                dtm2.addRow(new String[]{tempb.silinengiderler.get(i).urun_adi, tempb.silinengiderler.get(i).birim, String.valueOf(tempb.silinengiderler.get(i).miktar),
                    String.valueOf(tempb.silinengiderler.get(i).fiyat), String.valueOf(tempb.silinengiderler.get(i).toplamgider)});

            }
        }

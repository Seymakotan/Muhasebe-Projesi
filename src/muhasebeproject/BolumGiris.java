    private void jComboBolumlerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBolumlerActionPerformed
        // TODO add your handling code here:
        dtm3.setRowCount(0);
        Bolum tempb = new Bolum();
        for (int i = 0; i < FakulteGiris.fakulteGiris.bolumler.size(); i++) {
            if (jComboBolumler.getSelectedItem() != null) {
                if (FakulteGiris.fakulteGiris.bolumler.get(i).ad.equals(jComboBolumler.getSelectedItem().toString())) {
                    tempb = FakulteGiris.fakulteGiris.bolumler.get(i);

                }
            }
        }

        for (int i = 0; i < tempb.giderler.size(); i++) {
            if (tempb.giderler.get(i) != null) {
                // toplam += tempb.giderler.get(i).toplamgider;
                dtm3.addRow(new String[]{tempb.giderler.get(i).urun_adi, tempb.giderler.get(i).birim, String.valueOf(tempb.giderler.get(i).miktar),
                    String.valueOf(tempb.giderler.get(i).fiyat), String.valueOf(tempb.giderler.get(i).toplamgider)});
            }
        }

        for (int i = 0; i < Gider.onaylananBolumler.size(); i++) {
            if (jComboBolumler.getSelectedItem().toString().equals(Gider.onaylananBolumler.get(i).ad)) {
                jLabelonayy.setText("ONAYLANDI");
            } else {
                jLabelonayy.setText("ONAYLANMADI");
            }
        }
        giderTopla();

    }//GEN-LAST:event_jComboBolumlerActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            int miktar = Integer.parseInt(fakultemiktar.getText());
            int fiyat = Integer.parseInt(fakultefiyat.getText());
            //String urunadi = text_urunadi.getText();
            Gider g = new Gider();
            g.birim = jComboFakulteBirim.getSelectedItem().toString();
            g.fiyat = fiyat;
            g.miktar = miktar;
            g.urun_adi = jComboFakulte_Urun.getSelectedItem().toString();
            g.giderhesabı(g.fiyat, g.miktar);
            System.out.println("g totali" + g.toplamgider);

            Bolum bolumtemp = new Bolum();
            Fakulte f = FakulteGiris.fakulteGiris;
            for (int i = 0; i < f.bolumler.size(); i++) {
                if (f.bolumler.get(i).ad.equals(jComboBolumler.getSelectedItem().toString())) {
                    f.bolumler.get(i).giderler.add(g);
                    dtm3.addRow(new Object[]{g.urun_adi, g.birim, String.valueOf(g.miktar),
                        String.valueOf(g.fiyat), String.valueOf(g.toplamgider)});
                    fakultemiktar.setText("");
                    fakultefiyat.setText("");
                }
            }
        } catch (NumberFormatException n) {
            JOptionPane.showMessageDialog(this, "lutfen fiyat ve miktari sayi giriniz");
        }
        giderTopla();

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int k = jTable1.getSelectedRow();

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setValueAt(jComboFakulte_Urun.getSelectedItem(), k, 0);

        int total = Integer.parseInt(fakultemiktar.getText()) * Integer.parseInt(fakultefiyat.getText());
        if (k >= 0) {
            model.setValueAt(jComboFakulte_Urun.getSelectedItem(), k, 0);
            model.setValueAt(jComboFakulteBirim.getSelectedItem(), k, 1);
            model.setValueAt(fakultemiktar.getText(), k, 2);
            model.setValueAt(fakultefiyat.getText(), k, 3);
            model.setValueAt(total, k, 4);
        }

        Bolum bolumtemp = new Bolum();
        Fakulte f = FakulteGiris.fakulteGiris;
        for (int i = 0; i < f.bolumler.size(); i++) {
            if (f.bolumler.get(i).ad.equals(jComboBolumler.getSelectedItem().toString())) {
                bolumtemp = f.bolumler.get(i);
                for (int l = 0; l < bolumtemp.giderler.size(); l++) {
                    if (bolumtemp.giderler.get(l).urun_adi.equals(model.getValueAt(k, 0).toString())) {
                        bolumtemp.giderler.get(l).urun_adi = jComboFakulte_Urun.getSelectedItem().toString();
                        bolumtemp.giderler.get(l).birim = jComboFakulteBirim.getSelectedItem().toString();
                        bolumtemp.giderler.get(l).miktar = Integer.parseInt(fakultemiktar.getText());
                        bolumtemp.giderler.get(l).fiyat = Integer.parseInt(fakultefiyat.getText());
                        bolumtemp.giderler.get(l).giderhesabı(Integer.parseInt(fakultemiktar.getText()), Integer.parseInt(fakultefiyat.getText()));

                    }
                }

                fakultemiktar.setText("");
                fakultefiyat.setText("");

            }

            System.out.println(model.getValueAt(k, 0).toString());
            giderTopla();

        }
    }//GEN-LAST:event_jButton3ActionPerformed


    void giderTopla() {
        try {
            int toplam = 0;
            Bolum bolumtemp1 = new Bolum();

            for (int j = 0; j < FakulteGiris.fakulteGiris.bolumler.size(); j++) {

                if (FakulteGiris.fakulteGiris.bolumler.get(j).ad.equals(jComboBolumler.getSelectedItem().toString())) {
                    bolumtemp1 = FakulteGiris.fakulteGiris.bolumler.get(j);
                }
            }

            for (int i = 0; i < bolumtemp1.giderler.size(); i++) {
                toplam += bolumtemp1.giderler.get(i).toplamgider;
            }
            lbltoplam.setText(String.valueOf(toplam));

        } catch (NullPointerException n) {

        }
    }

    private void jComboFakulte_UrunActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboFakulte_UrunActionPerformed
        // TODO add your handling code here:
        jComboFakulteBirim.removeAllItems();

        String secilenurun = jComboFakulte_Urun.getSelectedItem().toString();
        for (int i = 0; i < Gider.urunListesi.size(); i++) {
            if (Gider.urunListesi.get(i).urun_adi.equals(secilenurun)) {
                for (int j = 0; j < Gider.urunListesi.get(i).birimler.length; j++) {
                    if (Gider.urunListesi.get(i).birimler[j] != null) {
                        jComboFakulteBirim.addItem(Gider.urunListesi.get(i).birimler[j]);
                    }
                }
            }
        }
    }//GEN-LAST:event_jComboFakulte_UrunActionPerformed

